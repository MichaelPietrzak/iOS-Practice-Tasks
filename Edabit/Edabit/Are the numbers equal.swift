import Foundation

// Are the Numbers Equal?
// Create a function that returns true when num1 is equal to num2; otherwise return false.

struct SameNum {
    func checkNum(_ num1: Int, _ num2: Int) -> Bool {
        num1 == num2 ? true : false
    }
}
